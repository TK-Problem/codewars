def my_first_interpreter(code):
    # Make your esolang interpreter here
    cell = 0
    messege = list()
    for symbol in code:
        if symbol == "+":
            cell += 1
            if cell == 256:
                cell = 0
        elif symbol == ".":
            messege.append(cell)
    return ''.join(chr(i) for i in messege)


